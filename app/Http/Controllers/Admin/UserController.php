     * @param \Illuminate\Http\Request $request
     * @param int $id
     * @return \Illuminate\Http\Response
     */
    public function role($current, $role, $id)
    {
        $user = User::find($id);
        $user->removeRole(trim($current, '[""]'));
        $role = Role::findById($role);
        $user->assignRole($role);
        return redirect()->back();
    }

